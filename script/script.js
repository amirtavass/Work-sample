   function selectedPage(page){
  let products=document.querySelector(".products");
   let nth=6*(page-1);
  let pageCount=productsData.length/6;
  products.innerHTML="<nav></nav>"; 
  for(let x=5+nth;x>=0+nth;x--){
  if(x<productsData.length){
       products.innerHTML=` <div class="product" onclick="showModal(${x})">
                           <img src="${productsData[x].picUrl}" alt="">
                          <div class="cover"></div>
                          <div class="textSection">
                              <span>مدل:</span>
                           <span>${productsData[x].name}</span>
                                </div>
                              </div> `+products.innerHTML;
}
}
let nav=document.querySelector(".products nav");
for(let i=0;i<pageCount;i++){
 nav.innerHTML+=`<span onClick="selectedPage(${i+1})">${i+1}</span>`;
}
document.querySelector(`.products nav span:nth-of-type(${page})`).classList.add("navItemActive");

}

function showModal(index){
   let modal= document.querySelector(".modal");
   let discriptions=modal.querySelectorAll(".discription");
   discriptions[0].innerHTML=productsData[index].name;
   let seperateDiscriptions=String(productsData[index].case).split("-");

   let s="";
   for(let x of seperateDiscriptions){
     s+=`<li>${x}</li>`;
     discriptions[1].innerHTML=s;
   }
   seperateDiscriptions=String(productsData[index].strap).split("-");

    s="";
   for(let x of seperateDiscriptions){
     s+=`<li>${x}</li>`;
     discriptions[2].innerHTML=s;
   }
   seperateDiscriptions=String(productsData[index].glasp).split("-");

   s="";
   for(let x of seperateDiscriptions){
     s+=`<li>${x}</li>`;
     discriptions[3].innerHTML=s;
   }
   modal.querySelector("img").src=productsData[index].picUrl;
  modal.style.opacity="1";
  modal.style.display="flex";
   setTimeout(()=>{
      document.querySelector(".modal .container").style.opacity="1";

   },500);
}
